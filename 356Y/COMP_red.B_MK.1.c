//catapult
void Catapult_launch(){
	while(SensorValue[pot] >= Initial_Value){
		motor[catapult1]=127;
		motor[catapult2]=127;
	}
	while(SensorValue[pot] <= Final_Value - Error_Margin){
		motor[catapult1]=127;
		motor[catapult2]=127;
	}
	motor[catapult1] = 50;
	motor[catapult2] = 50;
	wait1Msec(5);

	motor[catapult1] = 0;
	motor[catapult2] = 0;
}

//ballarm
void allarm(){
	while(SensorValue[pot2] >= Initial_Ballarm){
		motor[ballarm]=127;
	}
	while(SensorValue[pot2] <= Final_Ballarm - Error_Ballarm){
		motor[ballarm]=-127;
	}
	motor[ballarm] = -50;
	wait1Msec(5);

	motor[ballarm] = 0;
}

void moveUntilDistance(int distanceInDegrees)
{

	SensorValue(rightencoder) = 0;
	SensorValue(leftencoder) = 0;
	wait1Msec(100);

	while(SensorValue[rightencoder] < distanceInDegrees)
	{
		motor[rightback] = 100;
		motor[rightfront] = 100;
		motor[rightmiddle] = 100;
		motor[leftback] = 100;
		motor[leftfront] = 100;
		motor[leftmiddle] = 100;
	}
	motor[rightback] = 0;
	motor[rightfront] = 0;
	motor[rightmiddle] = 0;
	motor[leftback] = 0;
	motor[leftfront] = 0;
	motor[leftmiddle] = 0;
	wait1Msec(100);
}

void pointTurnRight(int turnDistanceRight)
{

	SensorValue(rightencoder) = 0;
	SensorValue(leftencoder) = 0;
	wait1Msec(100);

	while(-SensorValue[leftencoder] < turnDistanceRight)
	{
		motor[rightback] = -100;
		motor[rightfront] = -100;
		motor[rightmiddle] = -100;
		motor[leftback] = 100;
		motor[leftfront] = 100;
		motor[leftmiddle] = 100;
	}
	motor[rightback] = 0;
	motor[rightfront] = 0;
	motor[rightmiddle] = 0;
	motor[leftback] = 0;
	motor[leftfront] = 0;
	motor[leftmiddle] = 0;
	wait1Msec(100);
}

