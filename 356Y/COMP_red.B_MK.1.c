//catapult
task Catapult(){
	while(true){
		if (vexRT(Btn5UXmtr2)==1){
			while(SensorValue[pot] >= Initial_Value){
				motor[catapult1]=127;
				motor[catapult2]=127;
			}
			while(SensorValue[pot] <= Final_Value - Error_Margin){
				motor[catapult1]=127;
				motor[catapult2]=127;
			}
			motor[catapult1] = -50;
			motor[catapult2] = -50;
			wait1Msec(5);
			motor[catapult1] = 0;
			motor[catapult2] = 0;
		}
		else if (vexRT(Btn5DXmtr2)==1){
			motor[catapult1]=127;
			motor[catapult2]=127;
		}
		else{
			motor[catapult1] = 0;
			motor[catapult2] = 0;
		}
	}
}

//ballarm
task allarm(){
	while(true){
		if (vexRT(Btn8U)==1){ //Btn5UXmtr2
			while(SensorValue[pot2] >= Initial_Ballarm){
				motor[ballarm]=127;
			}
			while(SensorValue[pot2] <= Final_Ballarm - Error_Ballarm){
				motor[ballarm]=-127;
			}
			motor[ballarm] = -50;
			wait1Msec(5);

			motor[ballarm] = 0;
		}
		else if (vexRT(Btn8D)==1){//Btn5DXmtr2
			motor[ballarm]=-127;
		}
		else{
			motor[ballarm]=0;
		}
	}
}

